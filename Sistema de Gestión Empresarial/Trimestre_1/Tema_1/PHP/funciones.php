<?php

    function sumar($a, $b){
        $suma = $a + $b;
        return $suma;
    }

    function restar($a, $b){
        $resta = $a - $b;
        return $resta;
    }

    function multiplicar($a, $b){
        $multiplicacion = $a * $b;
        return $multiplicacion;
    }

    function dividir($a, $b){
        $division = $a / $b;
        return $division;
    }
?>